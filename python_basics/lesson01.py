# --- 1. print：把东西显示出来。括号里加引号的是"文字" ---
print("你好，我开始学 Python 了")

# --- 2. 变量：给一个值起个名字，之后用名字就代表那个值 ---
name = "小明"          # name 这个名字，现在代表文字 "小明"
age = 18               # age 现在代表数字 18（数字不加引号）
print(name)            # 显示 小明
print(age)             # 显示 18
print(name, age)       # 一起显示，中间自动空格

# --- 3. 数字可以算数 ---
a = 3
b = 5
print(a + b)           # 加法，显示 8
print(a * b)           # 乘法，显示 15

# --- 4. 列表 list：一排东西，用方括号 [] 装起来，是力扣里最常见的东西 ---
nums = [10, 20, 30]    # 一个有 3 个数字的列表
print(nums)            # 显示 [10, 20, 30]
print(nums[0])         # 取第 1 个，注意：编号从 0 开始！所以 [0] 是 10
print(nums[1])         # 取第 2 个，显示 20
print(len(nums))       # len = 长度，这个列表有 3 个东西，显示 3


